function show(tag) {
				var login = document.getElementById(tag);
				var fade = document.getElementById('fade');
				login.style.display = 'block';
				fade.style.display = 'block';
			}

			function hclasse(tag) {
				var login = document.getElementById(tag);
				var fade = document.getElementById('fade');
				login.style.display = 'none';
				fade.style.display = 'none';
			}